/**
 * Пример
 *
 * Найти наименьший корень биквадратного уравнения ax^4 + bx^2 + c = 0
 */
fun minBiRoot(a: Double, b: Double, c: Double): Double {
    // 1: в главной ветке if выполняется НЕСКОЛЬКО операторов
    if (a == 0.0) {
        if (b == 0.0) return Double.NaN // ... и ничего больше не делать
        val bc = -c / b
        if (bc < 0.0) return Double.NaN // ... и ничего больше не делать
        return -sqrt(bc)
        // Дальше функция при a == 0.0 не идёт
    }
    val d = discriminant(a, b, c)   // 2
    if (d < 0.0) return Double.NaN  // 3
    // 4
    val y1 = (-b + sqrt(d)) / (2 * a)
    val y2 = (-b - sqrt(d)) / (2 * a)
    val y3 = max(y1, y2)       // 5
    if (y3 < 0.0) return Double.NaN // 6
    return -sqrt(y3)           // 7
}

/**
 * Простая
 *
 * Мой возраст. Для заданного 0 < n < 200, рассматриваемого как возраст человека,
 * вернуть строку вида: «21 год», «32 года», «12 лет».
 */
fun ageDescription(age: Int): String {
    val lastNumAge = age % 10
    return when {
        lastNumAge == 1 && age !in 10..20 && age !in 111..114 -> "$age год"
        lastNumAge in 2..4 && age !in 10..20 && age !in 111..114 -> "$age года"
        else -> "$age лет"
    }
}

/**
 * Простая
 *
 * Путник двигался t1 часов со скоростью v1 км/час, затем t2 часов — со скоростью v2 км/час
 * и t3 часов — со скоростью v3 км/час.
 * Определить, за какое время он одолел первую половину пути?
 */
fun timeForHalfWay(t1: Double, v1: Double,
                   t2: Double, v2: Double,
                   t3: Double, v3: Double): Double {
    val s1 = v1 * t1
    val s2 = v2 * t2
    val s3 = v3 * t3
    val sHalf = (s1 + s2 + s3) / 2
    var tHalf = sHalf / v1

    if (tHalf <= t1) {
        return tHalf
    } else {
        tHalf = t1
    }

    val T2 = (sHalf - s1) / v2

    if (T2 <= t2) {
        tHalf += T2
        return tHalf
    } else {
        tHalf += t2
    }

    tHalf += ((sHalf - s1 - s2) / v3)
    return tHalf
}

/**
 * Простая
 *
 * Нa шахматной доске стоят черный король и две белые ладьи (ладья бьет по горизонтали и вертикали).
 * Определить, не находится ли король под боем, а если есть угроза, то от кого именно.
 * Вернуть 0, если угрозы нет, 1, если угроза только от первой ладьи, 2, если только от второй ладьи,
 * и 3, если угроза от обеих ладей.
 * Считать, что ладьи не могут загораживать друг друга
 */
fun whichRookThreatens(kingX: Int, kingY: Int,
                       rookX1: Int, rookY1: Int,
                       rookX2: Int, rookY2: Int): Int {

    val attackFrom1 = kingX == rookX1 || kingY == rookY1
    val attackFrom2 = kingX == rookX2 || kingY == rookY2

    return when {
        (!attackFrom1 && !attackFrom2) -> 0
        (attackFrom1 && !attackFrom2) -> 1
        (attackFrom2 && !attackFrom1) -> 2
        (attackFrom1 && attackFrom2) -> 3
        else -> -1
    }
}

/**
 * Простая
 *
 * На шахматной доске стоят черный король и белые ладья и слон
 * (ладья бьет по горизонтали и вертикали, слон — по диагоналям).
 * Проверить, есть ли угроза королю и если есть, то от кого именно.
 * Вернуть 0, если угрозы нет, 1, если угроза только от ладьи, 2, если только от слона,
 * и 3, если угроза есть и от ладьи и от слона.
 * Считать, что ладья и слон не могут загораживать друг друга.
 */
fun rookOrBishopThreatens(kingX: Int, kingY: Int,
                          rookX: Int, rookY: Int,
                          bishopX: Int, bishopY: Int): Int {
    val attackFromRook = kingX == rookX || kingY == rookY
    val attackFormBishop = abs(kingX - bishopX) == abs(kingY - bishopY)
    return when{
        (!attackFromRook && !attackFormBishop) -> 0
        (attackFromRook && !attackFormBishop) -> 1
        (attackFormBishop && !attackFromRook) -> 2
        (attackFormBishop && attackFromRook) -> 3
        else -> -1
    }
}

/**
 * Простая
 *
 * Треугольник задан длинами своих сторон a, b, c.
 * Проверить, является ли данный треугольник остроугольным (вернуть 0),
 * прямоугольным (вернуть 1) или тупоугольным (вернуть 2).
 * Если такой треугольник не существует, вернуть -1.
 */
fun triangleKind(a: Double, b: Double, c: Double): Int {
    val forA = (b * b + c * c - a * a)
    val forB = (a * a + c * c - b * b)
    val forC = (a * a + b * b - c * c)
    val aExist = b + c > a
    val bExist = a + c > b
    val cExist = a + b > c

    return when {
        (!cExist || !bExist || !aExist) -> -1
        (forA > 0.0 && forB > 0.0 && forC > 0.0) -> 0
        (forA == 0.0 || forB == 0.0 || forC == 0.0) -> 1
        else -> 2
    }
}

/**
 * Средняя
 *
 * Даны четыре точки на одной прямой: A, B, C и D.
 * Координаты точек a, b, c, d соответственно, b >= a, d >= c.
 * Найти длину пересечения отрезков AB и CD.
 * Если пересечения нет, вернуть -1.
 */
fun segmentLength(a: Int, b: Int, c: Int, d: Int): Int {
    return if (a in c..d && b in c..d) b - a
    else if (c in a..b && d in a..b) d - c
    else if (a in c..d && b !in c..d) d - a
    else if (a !in c..d && b in c..d) b - c
    else -1
}